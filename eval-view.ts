import * as fs from 'fs/promises';
import * as path from 'path';
import { fileURLToPath } from 'url';
import { createServer } from 'vite';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Ensure we are running this script correctly
const TESTS_DIR = path.join(__dirname, 'tests');

async function checkTestsDir() {
  try {
    await fs.access(TESTS_DIR);
    return true;
  } catch {
    return false;
  }
}

async function getDatasets() {
  const datasetPath = path.join(TESTS_DIR, 'eval_dataset');
  const datasets = new Map();
  try {
    const files = await fs.readdir(datasetPath);
    for (const file of files) {
      if (file.endsWith('.json')) {
        const content = await fs.readFile(path.join(datasetPath, file), 'utf-8');
        const data = JSON.parse(content);
        if (data.id) {
           datasets.set(data.id, data);
        }
      }
    }
  } catch (err) {
    console.warn(`Warning: Could not read eval_dataset dir: ${err}`);
  }
  return datasets;
}

async function getResults() {
  const resultsPath = path.join(TESTS_DIR, 'eval_results');
  const runs = [];
  try {
    const timestampDirs = await fs.readdir(resultsPath);
    for (const tsDir of timestampDirs) {
      if (tsDir.startsWith('.')) continue;
      const fullTsDirPath = path.join(resultsPath, tsDir);
      const stat = await fs.stat(fullTsDirPath);
      if (stat.isDirectory()) {
         const files = await fs.readdir(fullTsDirPath);
         
         // Parse report.md if it exists instead of .json files which might not exist
         const reportFile = files.find(f => f === 'report.md');
         if (reportFile) {
           const content = await fs.readFile(path.join(fullTsDirPath, reportFile), 'utf-8');
           // Simple parser for the markdown report generated by eval-compare.ts
           const summaryMatch = content.match(/## 总结\n- \*\*总用例数\*\*: (\d+)\n- \*\*通过数\*\*: (\d+)\n- \*\*失败数\*\*: (\d+)/);
           
           const results: any[] = [];
           const detailsSection = content.split('## 详情')[1] || '';
           const caseBlocks = detailsSection.split('### ').filter(Boolean);
           
           for (const block of caseBlocks) {
             const lines = block.trim().split('\n');
             if (lines.length === 0) continue;
             
             const headerMatch = lines[0].match(/(✅|❌)\s*(.+)/);
             if (headerMatch) {
               const status = headerMatch[1] === '✅' ? 'pass' : 'fail';
               const testId = headerMatch[2].trim();
               const reason = lines.slice(1).join('\n').trim();
               
               results.push({
                 test_id: testId,
                 status,
                 reason
               });
             }
           }
           
           if (summaryMatch) {
             runs.push({
               run_id: tsDir,
               timestamp: tsDir,
               summary: {
                 total: parseInt(summaryMatch[1], 10),
                 passed: parseInt(summaryMatch[2], 10),
                 failed: parseInt(summaryMatch[3], 10)
               },
               results
             });
           }
         }
      }
    }
  } catch (err) {
    console.warn(`Warning: Could not read eval_results dir: ${err}`);
  }
  return runs;
}

async function getInvocations() {
   const invokePath = path.join(TESTS_DIR, 'llm_invoke_results');
   const invocations = new Map();
   try {
     const timestampDirs = await fs.readdir(invokePath);
     for (const tsDir of timestampDirs) {
       if (tsDir.startsWith('.')) continue;
       const fullTsDirPath = path.join(invokePath, tsDir);
       const stat = await fs.stat(fullTsDirPath);
       if (stat.isDirectory()) {
         const files = await fs.readdir(fullTsDirPath);
         for (const file of files) {
           if (file.endsWith('.jsonl')) {
             const testId = file.replace('.jsonl', '');
             const content = await fs.readFile(path.join(fullTsDirPath, file), 'utf-8');
             const lines = content.split('\n').filter(Boolean);
             const messages = lines.map(line => {
               try { 
                 const obj = JSON.parse(line); 
                 if (obj.type === 'message' && obj.message) {
                   return {
                     role: obj.message.role,
                     content: obj.message.content,
                     timestamp: obj.timestamp,
                     source: obj
                   };
                 }
                 return null;
               } catch (e) { 
                 console.warn(`Warning: Could not parse line in ${file}`);
                 return null; 
               }
             }).filter(Boolean);
             invocations.set(testId, messages);
           }
         }
       }
     }
   } catch (err) {
     console.warn(`Notice: Could not read llm_invoke_results dir. Details: ${err}. The viewer will still function but details will be empty.`);
   }
   return invocations;
}

async function start() {
  const hasTests = await checkTestsDir();
  if (!hasTests) {
    console.error(`Error: 'tests' directory not found at ${TESTS_DIR}`);
    process.exit(1);
  }

  console.log('Loading local evaluation data...');
  const datasets = await getDatasets();
  const rawRuns = await getResults();
  const invocations = await getInvocations();

  const formattedRuns = rawRuns.map(run => {
    // Process results to form TestCase objects
    const cases = (run.results || []).map((res: any) => {
       const datasetInfo = datasets.get(res.test_id) || {};
       return {
         test_id: res.test_id,
         name: datasetInfo.name || res.test_id,
         description: datasetInfo.description || '',
         expected_behavior: datasetInfo.expected_behavior || '',
         status: res.status,
         score: res.score,
         judge_reason: res.reason,
         duration: res.duration_ms,
         has_logs: invocations.has(res.test_id),
         llm_messages: invocations.get(res.test_id) || []
       };
    });

    return {
      run_id: run.run_id,
      timestamp: run.timestamp || run.run_id,
      total: run.summary?.total || cases.length,
      passed: run.summary?.passed || cases.filter((c:any) => c.status === 'pass').length,
      failed: run.summary?.failed || cases.filter((c:any) => c.status === 'fail').length,
      cases
    };
  });
  
  // Sort runs by run_id (timestamp) descending
  formattedRuns.sort((a, b) => b.run_id.localeCompare(a.run_id));

  // Data to expose to Vite UI
  const invocationsObj = Object.fromEntries(invocations);

  const dataFilePath = path.join(__dirname, 'ui', 'public', 'eval-data.json');
  await fs.writeFile(dataFilePath, JSON.stringify({
    runs: formattedRuns,
    invocations: invocationsObj
  }));

  const vite = await createServer({
    root: path.join(__dirname, 'ui'),
    server: {
      port: 3000,
      open: true
    },
    plugins: [
      {
        name: 'remove-inject-placeholder',
        transformIndexHtml(html) {
          return html.replace('<!-- INJECT_DATA -->', '');
        }
      }
    ]
  });

  await vite.listen();
  console.log(`Viewer started at http://localhost:3000`);
}

start().catch(err => {
  console.error(err);
  process.exit(1);
});
